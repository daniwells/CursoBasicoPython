name = input('Type it your name: ')

if name[0] == 'A':
    print(f'Hello {name}, this name is too incredible!')
elif name[0] == 'B':
    print(f'Hello {name}, this name is beautiful')
elif name[0] == 'C':
    print(f'Hello {name}, this name is bad')
elif name[0] == 'D':
    print(f'Hello {name}, this name is awful')
else:
    print(f'Hello {name}, this name is ok')